    private boolean validaCampos(){
     if(txtEmail.getText().isEmpty()){
        JOptionPane.showMessageDialog(rootPane,"Campo email não pode ser vazio" );
        return false;
    }else if(txtSenha.getText().isEmpty()){
         JOptionPane.showMessageDialog(rootPane,"Campo senha não pode ser vazio" );
         return false;
    } else {
        return true;
    }
}
    public void limparCampos() {

            txtEmail.setText("");
            txtSenha.setText("");

    }

    private boolean checkProcurandoEmprego() {
            boolean cheked = false;
            if (jRadioProcurandoEmprego.isSelected()) {
                cheked = true;
            }
            return cheked;
        }

    private boolean checkProcurandoFuncionario() {
        boolean cheked = false;
        if (jRadioProcurandoFunc.isSelected()) {
            cheked = true;
        }
        return cheked;
    }

    
    private boolean campoSelecionado(){
        int tipoUsuario = 0;
        boolean selecionado = false;
        if (jRadioProcurandoEmprego.isSelected()) {
            selecionado = true;
            tipoUsuario = 2;
            this.dispose();
        }else if (jRadioProcurandoFunc.isSelected()) {
            tipoUsuario = 1;
            this.dispose();
            selecionado = true;
        } 
            return selecionado;
    }
    
    private int getTipoUser(){
        int tipoUser=0;
        if (jRadioProcurandoEmprego.isSelected()) {
           tipoUser = 2;
        }else if (jRadioProcurandoFunc.isSelected()) {
           tipoUser = 1;
        } 
        return tipoUser;
    }
