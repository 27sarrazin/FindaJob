    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        Usuario user = new Usuario();
            UsuarioDao dao = new UsuarioDao();
        if(validaCampos() == false){
            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha todos os campos obrigatórios!");
        }else if(campoSelecionado()== false){
            JOptionPane.showMessageDialog(this, "Por favor, você precisa selecionar uma opção antes de continuar.");
            
        }else{
            if(dao.verificarSeUserCadastrado(txtEmail.getText())){
             
                 JOptionPane.showMessageDialog(this, "Usuário já cadastrado!");
                
            }else{
                 user.setEmail(txtEmail.getText());
                 user.setSenha(txtSenha.getText());
                 user.setTipo_usuario(getTipoUser());
            
                dao.cadastrar(user);
            
                JOptionPane.showMessageDialog(rootPane, "Usuario cadastrado com sucesso!");
                limparCampos(); 
            }
        }  
       
    }//GEN-LAST:event_btnCriarActionPerformed

