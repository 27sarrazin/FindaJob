    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        Usuario usuario = new Usuario();
        UsuarioDao dao = new UsuarioDao();
        CandidatoDao candidatoDao = new CandidatoDao();
        EmpresaDao empresaDao = new EmpresaDao();
        
        if(validaCampos() == false){
            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha todos os campos obrigat√≥rios!");
        }else{
        
        
        usuario.setEmail(txtEmail.getText());
        usuario.setSenha(txtSenha.getText());
        usuario  = dao.validateUser(usuario);
        
        
        /*
            1 para empresa
            2 para funcionario
        */
            if (usuario != null) {
                if(usuario.getTipo_usuario() == 1){
                    if(empresaDao.verificarSeEmpresaCadastrada(usuario.getId_usuario())){
                        telaPrincipalEmpresa menuEmpresa = new telaPrincipalEmpresa();
                        menuEmpresa.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(null, "Bem Vindo, voc√™ acessou a p√°gina da empresa!");
                        CadastroEmpresa cadEmpresa = new CadastroEmpresa();
                        cadEmpresa.setVisible(true);

                        this.dispose();
                    }
            }else if(usuario.getTipo_usuario() == 2){
                if(candidatoDao.verificarSeCandidatoCadastrado(usuario.getId_usuario())){
                    TelaPrincipalCandidato menuCandidato = new TelaPrincipalCandidato(usuario);
                    menuCandidato.setVisible(true);
                }else{
                   JOptionPane.showMessageDialog(null, "Bem Vindo, voc√™ acessou a p√°gina de candidato!");
                    CadastroTrabalhador cadTrabalhador = new CadastroTrabalhador(usuario.getEmail(),usuario.getId_usuario(),usuario.getTipo_usuario(),usuario.getSenha());
                    cadTrabalhador.setVisible(true);
                    this.dispose(); 
                }
                
            }
            }else {
                    JOptionPane.showMessageDialog(null, "Login inv√°lido!");
            }
        }
   
    }//GEN-LAST:event_btnEntrarActionPerformed

