  private boolean validaCampos(){
    if(txtEmail.getText().isEmpty()){
        JOptionPane.showMessageDialog(rootPane,"Campo email n√£o pode ser vazio" );
        return false;
    }else if(txtSenha.getText().isEmpty()){
         JOptionPane.showMessageDialog(rootPane,"Campo senha n√£o pode ser vazio" );
         return false;
    } else {
        return true;
    }
}
