    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos() == false) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha todos os campos obrigatórios!");
        } else {
          
            FormacaoAcademica formacao = new FormacaoAcademica();
            FormacaoDao dao = new FormacaoDao();
       
            formacao.setCurso(txtCurso.getText());
            formacao.setInstituicao(txtInstituicao.getText());
            formacao.setCidade(txtCidade.getText());
            formacao.setId_Candidato(candidato);
            
            dao.salvar(formacao);
            JOptionPane.showMessageDialog(rootPane, "Cadastro do candidato realizado com sucesso!");
       
            limpaCampos();
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
       
        String novoCurso = txtCurso.getText();
        String novaInstituicao = txtInstituicao.getText();
        String novaCidade = txtCidade.getText();
         
        if (novoCurso.isEmpty() || novaInstituicao.isEmpty() || novaCidade.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            FormacaoAcademica formacao = new FormacaoAcademica();
            System.out.println("CURSO PEGO>>>"+novoCurso);
            System.out.println("Id PEGO>>>"+id_Formacao);
            formacao.setIdFormacao( id_Formacao);
            formacao.setCurso(novoCurso);
            formacao.setInstituicao(novaInstituicao);
            formacao.setCidade(novaCidade);
            formacao.setId_Candidato(candidato);

            if (candidato == null) {
                JOptionPane.showMessageDialog(this, "Candidato não encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
           
            FormacaoDao formDao = new FormacaoDao();
            formDao.atualizar(formacao);

            JOptionPane.showMessageDialog(this, "Formação acadêmica atualizada com sucesso!");

            this.dispose();

        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

