    private boolean validaCampos(){
        if(txtCurso.getText().isEmpty()){
           JOptionPane.showMessageDialog(rootPane,"Campo Grau/Curso não pode ser vazio" );
           return false;
       }else if(txtInstituicao.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Campo Instituição não pode ser vazio" );
            return false;
       }else if(txtCidade.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"Campo Cidade não pode ser vazio" );
            return false;
       } else {
           return true;
       }
    }
    public void limpaCampos() {
        
        txtCurso.setText("");
        txtInstituicao.setText("");
        txtCidade.setText("");
        
        
    }


